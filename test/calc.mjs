/**
 * Add 2 numbers
 * @param {Int} a 
 * @param {Int} b 
 * @returns a + b
 */
const Add = (a, b) => {
    return a + b;
}

/**
 * Substract 2 numbers
 * @param {Int} a 
 * @param {Int} b 
 * @returns 
 */
const Substract = (a, b) => {
    return a - b;
}

/**
 * Multiply 2 numbers
 * @param {Int} a 
 * @param {Int} b 
 * @returns multiplication
 */
const Multiply = (a, b) => {
    return a*b;
}

export { Add, Substract, Multiply }